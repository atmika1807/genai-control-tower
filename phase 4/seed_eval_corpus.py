# Populated by seed_eval_corpus() — maps doc_id → chunk_id
_DOC_TO_CHUNK: dict[str, str] = {}


async def build_retriever_and_reranker():
    """Build retriever and reranker pointed at the test Qdrant instance."""
    from unittest.mock import patch

    # Patch settings to use test collection
    os.environ.setdefault("COLLECTION_NAME", "ci_eval_corpus")

    from query.retriever import HybridRetriever
    from query.reranker import CrossEncoderReranker

    retriever = HybridRetriever()
    reranker  = CrossEncoderReranker()
    return retriever, reranker


async def seed():
    """Ingest the synthetic corpus and resolve chunk IDs in EVAL_DATASET."""
    os.environ.setdefault("COLLECTION_NAME", "ci_eval_corpus")

    chunker  = ChunkingEngine(chunk_size=256, chunk_overlap=32)
    embedder = EmbeddingService()
    vs       = VectorStoreClient()

    await vs.ensure_collection()

    for doc in CORPUS:
        chunks   = chunker.chunk(doc["text"], doc_id=doc["doc_id"], metadata=doc["metadata"])
        embedded = await embedder.embed_chunks(chunks)
        await vs.upsert(embedded)

        # Record first chunk ID for eval resolution
        if chunks:
            _DOC_TO_CHUNK[doc["doc_id"]] = chunks[0].chunk_id

    # Resolve __resolve__:doc_id placeholders in EVAL_DATASET
    for sample in EVAL_DATASET:
        resolved = []
        for cid in sample["relevant_chunk_ids"]:
            if cid.startswith("__resolve__:"):
                doc_id = cid.split(":", 1)[1]
                resolved.append(_DOC_TO_CHUNK.get(doc_id, cid))
            else:
                resolved.append(cid)
        sample["relevant_chunk_ids"] = resolved

    print(f"Seeded {len(CORPUS)} docs, resolved {len(EVAL_DATASET)} eval samples")


if __name__ == "__main__":
    asyncio.run(seed())
